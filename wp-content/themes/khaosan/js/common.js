$(function () {
	/* -------------------------------------------------------------------------------------
	   メニュー
	   ------------------------------------------------------------------------------------- */
	// PC用
	$("#toggle .toggle").on("click", function () {
		$(this).next().slideToggle(300);
	});

	// スマホ用
	$("#menu").mmenu();


	/* -------------------------------------------------------------------------------------
	   ページトップ
	   ------------------------------------------------------------------------------------- */	
	// 表示
	var topBtn = $("#page-top");
	topBtn.hide();
	$(window).scroll(function () {
		if ($(this).scrollTop() > 500) {
			topBtn.fadeIn();
		} else {
			topBtn.fadeOut();
		}
	});
	
	// 移動
	topBtn.click(function () {
		$("html, body").animate({
			scrollTop: 0
		}, 500);
		return false;
	});

});